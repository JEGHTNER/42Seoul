#include "libft.h"

void	ft_bzero(void *ptr, size_t len)
{
	char	*tmp;
	size_t	i;

	tmp = (char *)ptr;
	i = 0;
	while (i < len)
	{
		tmp[i] = 0;
		i++;
	}
}
